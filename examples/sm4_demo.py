def sm4_encrypt_block(plaintext, key):
    """
    SM4 加密一个分组 (128 bit)

    输入:
        plaintext: 16字节明文
        key: 16字节密钥
    输出:
        16字节密文

    流程:

    明文 ──► X0,X1,X2,X3
                │
                ▼
         ┌─────────────┐
         │  32轮迭代    │
         │             │
         │ 第i轮:      │
         │ Xi+4 = Xi ⊕ │
         │   T(Xi+1⊕   │
         │     Xi+2⊕   │
         │     Xi+3⊕   │
         │     rki)    │
         └──────┬──────┘
                │
                ▼
         X32,X33,X34,X35
                │
                ▼ (反序)
         X35,X34,X33,X32
                │
                ▼
             密文
    """
    # 生成轮密钥
    rk = expand_key(key)

    # 明文转换为4个32位字
    X = bytes_to_words(plaintext)

    print("\n" + "="*60)
    print("SM4 加密过程跟踪")
    print("="*60)
    print(f"明文: {plaintext.hex()}")
    print(f"密钥: {key.hex()}")
    print("-"*60)

    # 32轮迭代
    for i in range(32):
        # Xi+4 = Xi ⊕ T(Xi+1 ⊕ Xi+2 ⊕ Xi+3 ⊕ rki)
        tmp = X[1] ^ X[2] ^ X[3] ^ rk[i]
        new_x = X[0] ^ T(tmp)

        if i < 4 or i >= 28:  # 只打印前4轮和后4轮
            print(f"轮 {i:2d}: X[{i}]={X[0]:08X} ⊕ T({X[1]:08X}⊕{X[2]:08X}⊕{X[3]:08X}⊕rk[{i}]={rk[i]:08X})")
            print(f"        => X[{i+4}] = {new_x:08X}")
        elif i == 4:
            print("        ... (省略中间轮次) ...")

        # 更新状态
        X = [X[1], X[2], X[3], new_x]

    # 反序输出
    result = words_to_bytes([X[3], X[2], X[1], X[0]])

    print("-"*60)
    print(f"密文: {result.hex()}")
    print("="*60)

    return result


def sm4_decrypt_block(ciphertext, key):
    """
    SM4 解密一个分组

    解密与加密结构相同，只是轮密钥使用顺序相反
    加密: rk0, rk1, ..., rk31
    解密: rk31, rk30, ..., rk0
    """
    # 生成轮密钥
    rk = expand_key(key)

    # 密文转换为4个32位字
    X = bytes_to_words(ciphertext)

    # 32轮迭代 (轮密钥反序使用)
    for i in range(32):
        tmp = X[1] ^ X[2] ^ X[3] ^ rk[31 - i]  # 反序使用轮密钥
        new_x = X[0] ^ T(tmp)
        X = [X[1], X[2], X[3], new_x]

    # 反序输出
    return words_to_bytes([X[3], X[2], X[1], X[0]])


