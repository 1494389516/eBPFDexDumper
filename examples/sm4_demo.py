# ============================================================================
#                              基础操作
# ============================================================================

def rotl(x, n):
    """32位循环左移"""
    return ((x << n) | (x >> (32 - n))) & 0xFFFFFFFF


def bytes_to_words(data):
    """16字节 -> 4个32位字 (大端)"""
    return [
        (data[i*4] << 24) | (data[i*4+1] << 16) | (data[i*4+2] << 8) | data[i*4+3]
        for i in range(4)
    ]


def words_to_bytes(words):
    """4个32位字 -> 16字节 (大端)"""
    result = []
    for w in words:
        result.extend([
            (w >> 24) & 0xFF,
            (w >> 16) & 0xFF,
            (w >> 8) & 0xFF,
            w & 0xFF
        ])
    return bytes(result)


