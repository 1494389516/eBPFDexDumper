def test_sm4():
    """
    使用官方测试向量验证实现正确性

    来自 GM/T 0002-2012 标准
    """
    print("\n" + "="*60)
    print("SM4 官方测试向量验证")
    print("="*60)

    # 测试向量 (来自国密标准)
    key = bytes.fromhex("0123456789ABCDEFFEDCBA9876543210")
    plaintext = bytes.fromhex("0123456789ABCDEFFEDCBA9876543210")
    expected_ciphertext = bytes.fromhex("681EDF34D206965E86B3E94F536E4246")

    # 加密测试
    ciphertext = sm4_encrypt_block(plaintext, key)

    print(f"\n预期密文: {expected_ciphertext.hex()}")
    print(f"实际密文: {ciphertext.hex()}")
    print(f"加密测试: {'✓ 通过' if ciphertext == expected_ciphertext else '✗ 失败'}")

    # 解密测试
    decrypted = sm4_decrypt_block(ciphertext, key)
    print(f"\n解密结果: {decrypted.hex()}")
    print(f"原始明文: {plaintext.hex()}")
    print(f"解密测试: {'✓ 通过' if decrypted == plaintext else '✗ 失败'}")

    print("\n" + "="*60)
    print("轮密钥展示 (白盒攻击的目标)")
    print("="*60)
    rk = expand_key(key)
    for i in range(32):
        print(f"rk[{i:2d}] = 0x{rk[i]:08X}")


