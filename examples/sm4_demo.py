def tau(A):
    """
    非线性变换 τ
    将32位输入拆成4字节，分别过S盒

        A (32-bit)
           │
           ▼
    ┌──┬──┬──┬──┐
    │a0│a1│a2│a3│
    └──┴──┴──┴──┘
       │  │  │  │
       ▼  ▼  ▼  ▼
    ┌──┬──┬──┬──┐
    │S │S │S │S │  ← S-box
    └──┴──┴──┴──┘
       │  │  │  │
       ▼  ▼  ▼  ▼
    ┌──┬──┬──┬──┐
    │b0│b1│b2│b3│
    └──┴──┴──┴──┘
           │
           ▼
        B (32-bit)
    """
    b0 = SBOX[(A >> 24) & 0xFF]
    b1 = SBOX[(A >> 16) & 0xFF]
    b2 = SBOX[(A >> 8) & 0xFF]
    b3 = SBOX[A & 0xFF]
    return (b0 << 24) | (b1 << 16) | (b2 << 8) | b3


def L(B):
    """
    线性变换 L (用于加密)

    L(B) = B ⊕ (B<<<2) ⊕ (B<<<10) ⊕ (B<<<18) ⊕ (B<<<24)

           B
           │
    ┌──────┼──────┬──────┬──────┬──────┐
    │      │      │      │      │      │
    │      ▼      ▼      ▼      ▼      ▼
    │      B    B<<<2  B<<<10 B<<<18 B<<<24
    │      │      │      │      │      │
    │      └──────┴──────┴──────┴──────┘
    │                    │
    │                    ▼
    │                   XOR
    │                    │
    └────────────────────┘
                         │
                         ▼
                        C
    """
    return B ^ rotl(B, 2) ^ rotl(B, 10) ^ rotl(B, 18) ^ rotl(B, 24)


def L_prime(B):
    """
    线性变换 L' (用于密钥扩展)

    L'(B) = B ⊕ (B<<<13) ⊕ (B<<<23)
    """
    return B ^ rotl(B, 13) ^ rotl(B, 23)


def T(x):
    """
    轮函数 T = L ∘ τ

        x (32-bit)
           │
           ▼
       ┌───────┐
       │   τ   │  非线性变换 (S盒)
       └───┬───┘
           │
           ▼
       ┌───────┐
       │   L   │  线性变换
       └───┬───┘
           │
           ▼
        T(x)
    """
    return L(tau(x))


def T_prime(x):
    """密钥扩展用的 T' = L' ∘ τ"""
    return L_prime(tau(x))


# ============================================================================
#                              密钥扩展
# ============================================================================

def expand_key(key):
    """
    从128位主密钥生成32个轮密钥

    主密钥 MK (128-bit)
           │
           ▼
    ┌───┬───┬───┬───┐
    │MK0│MK1│MK2│MK3│
    └─┬─┴─┬─┴─┬─┴─┬─┘
      │   │   │   │
      ▼   ▼   ▼   ▼
    ┌───┬───┬───┬───┐
    │⊕  │⊕  │⊕  │⊕  │ ← 与 FK 异或
    │FK0│FK1│FK2│FK3│
    └─┬─┴─┬─┴─┬─┴─┬─┘
      │   │   │   │
      ▼   ▼   ▼   ▼
    ┌───┬───┬───┬───┐
    │K0 │K1 │K2 │K3 │
    └───┴───┴───┴───┘
           │
           ▼
    ┌─────────────────┐
    │  32轮迭代生成    │
    │  rki = ...      │
    └────────┬────────┘
             │
             ▼
    rk0, rk1, ... , rk31
    """
    # 将密钥转换为4个32位字
    MK = bytes_to_words(key)

    # 与FK异或得到K
    K = [MK[i] ^ FK[i] for i in range(4)]

    # 生成32个轮密钥
    rk = []
    for i in range(32):
        # rki = Ki ⊕ T'(Ki+1 ⊕ Ki+2 ⊕ Ki+3 ⊕ CKi)
        tmp = K[1] ^ K[2] ^ K[3] ^ CK[i]
        rk_i = K[0] ^ T_prime(tmp)
        rk.append(rk_i)
        # 更新K
        K = [K[1], K[2], K[3], rk_i]

    return rk


